CREATE TABLE LIBRARIAN
(
	MA_LIB VARCHAR(255) NOT NULL PRIMARY KEY,
	HO_TEN NCHAR(255) NOT NULL,
	DIA_CHI NCHAR(255) NOT NULL,
	SDT CHAR(13) NOT NULL
)

CREATE TABLE NGAY_TRUC
(
	MA_LIB VARCHAR(255) NOT NULL REFERENCES LIBRARIAN(MA_LIB),
	MA_CA_TRUC SMALLINT NOT NULL PRIMARY KEY,
)

CREATE TABLE CHI_TIET_CA
(
	MA_CA_TRUC SMALLINT NOT NULL PRIMARY KEY REFERENCES NGAY_TRUC(MA_CA_TRUC),
	NGAY_DAU DATE NOT NULL,
	NGAY_CUOI DATE NOT NULL,
)

CREATE TABLE TUSACH
(
	ID_TU SMALLINT NOT NULL ,
	TANG SMALLINT NOT NULL,
	THELOAI NCHAR(255) NOT NULL,
	NGONNGU NCHAR(255) NOT NULL,
	PRIMARY KEY (ID_TU, TANG)
) 

CREATE TABLE TACGIA
(
	ID_TACGIA VARCHAR(255) NOT NULL PRIMARY KEY,
	TEN_TACGIA NCHAR(255) NOT NULL,
)

create table TUASACH 
(
	MA_TUASACH VARCHAR(255) NOT NULL ,
	TUASACH NCHAR(255) NOT NULL ,
	ID_TACGIA VARCHAR(255) NOT NULL REFERENCES TACGIA(ID_TACGIA),
	THELOAI NCHAR(255) NOT NULL,
	PRIMARY KEY (MA_TUASACH)
)

CREATE TABLE DAUSACH 
(
	ISBN INT NOT NULL ,
	MA_TUASACH VARCHAR(255) NOT NULL REFERENCES TUASACH(MA_TUASACH),
	ID_TU SMALLINT NOT NULL ,
	TANG SMALLINT NOT NULL ,
	NGONNGU NCHAR(15) NOT NULL,
	BIA NCHAR(15),
	TRANGTHAI NCHAR(255),
	SOLUONG INT NOT NULL,
	PRIMARY KEY (ISBN),
	CONSTRAINT FK_DAUSACH_CREATED_BY FOREIGN KEY(ID_TU, TANG) REFERENCES [TUSACH](ID_TU, TANG)
)

CREATE TABLE CUONSACH
(
	ISBN INT NOT NULL REFERENCES DAUSACH(ISBN),
	MA_TUASACH VARCHAR(255) NOT NULL REFERENCES TUASACH(MA_TUASACH),
	CONLAI SMALLINT NOT NULL ,
	PRIMARY KEY (ISBN, MA_TUASACH)
	
)

CREATE TABLE DOCGIA
(
	MA_DOCGIA INT NOT NULL,
	HO_TEN NCHAR(35) NOT NULL,
	HINH IMAGE,
	TIEN_PHAT MONEY,
	TIEN_DATRA MONEY,
	PRIMARY KEY (MA_DOCGIA)
)

CREATE TABLE NGUOILON
(
	MA_DOCGIA INT NOT NULL REFERENCES DOCGIA(MA_DOCGIA),
	DIACHI NCHAR(255) NOT NULL,
	DIENTHOAI CHAR(13) NOT NULL,
	HAN_SD DATE NOT NULL,
	PRIMARY KEY (MA_DOCGIA)
)

CREATE TABLE TREEM
(
	MA_DOCGIA INT NOT NULL REFERENCES DOCGIA(MA_DOCGIA),
	MA_DOC_GIA_BAOLANH INT NOT NULL REFERENCES NGUOILON(MA_DOCGIA),
	DOB DATE NOT NULL,
	PRIMARY KEY (MA_DOCGIA)
)

CREATE TABLE DANGKY
(
	ISBN INT NOT NULL REFERENCES DAUSACH(ISBN),
	MA_DOCGIA INT NOT NULL REFERENCES DOCGIA (MA_DOCGIA),
	NGAY_DK DATE,
	GHICHU NCHAR(255),
	PRIMARY KEY (ISBN, MA_DOCGIA)
)


CREATE TABLE QTRINHMUON
(
	ISBN INT NOT NULL REFERENCES DAUSACH(ISBN),
	MA_TUASACH VARCHAR(255) NOT NULL REFERENCES TUASACH(MA_TUASACH),
	NGAY_MUON DATE NOT NULL ,
	MA_DOCGIA INT NOT NULL REFERENCES DOCGIA(MA_DOCGIA),
	MA_LIB VARCHAR(255) NOT NULL REFERENCES LIBRARIAN(MA_LIB),
	PRIMARY KEY (ISBN, NGAY_MUON)
)

CREATE TABLE NGAYMUON
(
	MA_MUON INT NOT NULL PRIMARY KEY,
	ISBN INT NOT NULL REFERENCES DAUSACH(ISBN),
	NGAY_MUON DATE NOT NULL,
	NGAY_HETHAN DATE,
	NGAY_TRA DATE,
	TIEN_PHAT MONEY,
	TIEN_DATRA MONEY,
	GHI_CHU NCHAR(255),
)


CREATE TABLE MUONSACH
(
	MA_DOCGIA INT NOT NULL PRIMARY KEY REFERENCES DOCGIA(MA_DOCGIA),
	SO_SACH_MUON SMALLINT NOT NULL,
	TIEN_PHAT MONEY ,
	SACH_LAM_MAT SMALLINT ,

